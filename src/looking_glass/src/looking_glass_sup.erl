-module(looking_glass_sup).
-behaviour(supervisor).

-export([start_link/0]).
-export([init/1]).

start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

init([]) ->
    Procs = [
        {lg_rabbit_hole, {lg_rabbit_hole, start_link, []},
            permanent, 5000, worker, [lg_rabbit_hole]}
    ],
    {ok, {{one_for_one, 1, 5}, Procs}}.
